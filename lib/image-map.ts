export function resolveImage(
  slug: string,
  destination?: string | null,
  category?: string | null,
  existingImage?: string | null,
): string {
  if (existingImage && !existingImage.includes('placeholder') && !existingImage.includes('generic')) {
    return existingImage
  }
  if (slug && SLUG_IMAGES[slug]) return SLUG_IMAGES[slug]
  if (destination) {
    const key = destination.toLowerCase().replace(/[^a-z]/g, '')
    if (DESTINATION_IMAGES[key]) return DESTINATION_IMAGES[key]
  }
  if (category && CATEGORY_IMAGES[category]) return CATEGORY_IMAGES[category]
  const destKey = slug?.split('-')[0]?.toLowerCase()
  if (destKey && DESTINATION_IMAGES[destKey]) return DESTINATION_IMAGES[destKey]
  return CATEGORY_IMAGES['Carnets Voyage']
}
